public class CourseBean {
    private String CourseName;
    private String CourseWeeks;
    private String CourseNum;
    private String CourseBuilding;
    private String CoursePlace;
    private String CourseTeacher;
    private int CourseDay;

    public int getCourseDay() {
        return CourseDay;
    }

    public void setCourseDay(int courseDay) {
        CourseDay = courseDay;
    }

    public String getCourseName() {
        return CourseName;
    }

    public void setCourseName(String courseName) {
        CourseName = courseName;
    }

    public String getCourseWeeks() {
        return CourseWeeks;
    }

    public void setCourseWeeks(String courseWeeks) {
        CourseWeeks = courseWeeks;
    }

    public String getCourseNum() {
        return CourseNum;
    }

    public void setCourseNum(String courseNum) {
        CourseNum = courseNum;
    }

    public String getCourseBuilding() {
        return CourseBuilding;
    }

    public void setCourseBuilding(String courseBuilding) {
        CourseBuilding = courseBuilding;
    }

    public String getCoursePlace() {
        return CoursePlace;
    }

    public void setCoursePlace(String coursePlace) {
        CoursePlace = coursePlace;
    }

    public String getCourseTeacher() {
        return CourseTeacher;
    }

    public void setCourseTeacher(String courseTeacher) {
        CourseTeacher = courseTeacher;
    }
}
